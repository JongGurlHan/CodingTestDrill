package com.Codeup;

import java.util.Scanner;

public class CodeUp1075 {
  public static void main(String[] args) {

  	Scanner sc = new Scanner(System.in);
  	
  	int num = sc.nextInt();
	
  	while(true) {
		
  		num--;		
  		
  		System.out.println(num);

  		if(num == 0) {
			break;
		}		

  	}
  	
  	
  }
}      

